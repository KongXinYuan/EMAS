public class StatisticsRequest {
	private Long branchid;
	private String chartType;
	private String statisType;
	private String startYear;
	private String endYear;
	private String chooseYear;
	private String startMonth;
	private String endMonth;
	public Long getBranchid() {
		return branchid;
	}
	public void setBranchid(Long branchid) {
		this.branchid = branchid;
	}
	public String getChartType() {
		return chartType;
	}
	public void setChartType(String chartType) {
		this.chartType = chartType;
	}
	public String getStatisType() {
		return statisType;
	}
	public void setStatisType(String statisType) {
		this.statisType = statisType;
	}
	public String getStartYear() {
		return startYear;
	}
	public void setStartYear(String startYear) {
		this.startYear = startYear;
	}
	public String getEndYear() {
		return endYear;
	}
	public void setEndYear(String endYear) {
		this.endYear = endYear;
	}
	public String getChooseYear() {
		return chooseYear;
	}
	public void setChooseYear(String chooseYear) {
		this.chooseYear = chooseYear;
	}
	public String getStartMonth() {
		return startMonth;
	}
	public void setStartMonth(String startMonth) {
		this.startMonth = startMonth;
	}
	public String getEndMonth() {
		return endMonth;
	}
	public void setEndMonth(String endMonth) {
		this.endMonth = endMonth;
	}
}
