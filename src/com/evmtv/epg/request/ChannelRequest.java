package com.evmtv.epg.request;

public class ChannelRequest {
	private int s1;
	private int s2;
	private Long fbranchid;
	/**
	 * @return the s1
	 */
	public int getS1() {
		return s1;
	}
	/**
	 * @param s1 the s1 to set
	 */
	public void setS1(int s1) {
		this.s1 = s1;
	}
	/**
	 * @return the s2
	 */
	public int getS2() {
		return s2;
	}
	/**
	 * @param s2 the s2 to set
	 */
	public void setS2(int s2) {
		this.s2 = s2;
	}
	/**
	 * @return the fbranchid
	 */
	public Long getFbranchid() {
		return fbranchid;
	}
	/**
	 * @param fbranchid the fbranchid to set
	 */
	public void setFbranchid(Long fbranchid) {
		this.fbranchid = fbranchid;
	}
}
